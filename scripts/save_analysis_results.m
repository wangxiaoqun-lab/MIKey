function save_analysis_results(dest, subject_networks, group_idx, ...
        diff_p, diff_t, sig_connections, global_metrics, ...
        node_strength, sig_nodes, node_t, intra_conn, inter_conn)
    
    save(fullfile(dest, 'analysis_results.mat'), ...
        'subject_networks', 'group_idx', ...
        'diff_p', 'diff_t', 'sig_connections', ...
        'global_metrics', 'node_strength', ...
        'sig_nodes', 'node_t', 'intra_conn', 'inter_conn');
    
    T_diff = array2table(sig_connections, ...
        'VariableNames', {'ROI_i', 'ROI_j', 't_value', 'p_value'});
    writetable(T_diff, fullfile(dest, 'significant_connections.csv'));
    
    T_nodes = table(sig_nodes, node_t(sig_nodes), ...
        'VariableNames', {'ROI', 't_value'});
    writetable(T_nodes, fullfile(dest, 'significant_nodes.csv'));
end